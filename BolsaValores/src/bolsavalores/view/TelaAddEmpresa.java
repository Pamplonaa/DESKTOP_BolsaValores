    public boolean testaEhInt(String s){
        boolean ehNumero = true;
        for(int i =0; i< s.length(); i++){
            if(!Character.isDigit(s.charAt(i))){
               ehNumero = false;
               break;
            }
        }
        return ehNumero;
    }
    
    public boolean testaEhDouble(String s){
     boolean ehNumero = true;
        for(int i =0; i< s.length(); i++){
            if(!Character.isDigit(s.charAt(i)) && !(s.charAt(i) == ',')){
               ehNumero = false;
               break;
            }
        }
        
        return ehNumero;
    }
